package fr.tao.bankaccount.enums;

/**
 * Enum representing types of banking operations :
 * - {@link OperationType#DEPOSIT}: Represents a deposit operation.
 * - {@link OperationType#WITHDRAWAL}: Represents a withdrawal operation.
 * - {@link OperationType#TRANSFER}: Represents a transfer operation between accounts.
 * 
 * @see Operation
 * 
 * @author T. Aoufi
 * @version 1.0
 * @since 17/03/2024 
 */
public enum OperationType {
	
	DEPOSIT,
	WITHDRAWAL,
	TRANSFER

}
